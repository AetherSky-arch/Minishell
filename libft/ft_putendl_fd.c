#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	backslash;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	backslash = '\n';
	write(fd, &backslash, 1);
}
