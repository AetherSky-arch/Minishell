#include "libft.h"

void	double_print(char **tab)
{
	int	i;

	if (tab == NULL)
	{
		ft_printf("(nil)");
		return ;
	}
	i = 0;
	while (tab[i] != NULL)
	{
		ft_printf("%s\n", tab[i]);
		i++;
	}
}
